# 資料：資料的基本單位

# 數字
3456
3.5 # 浮點數

# 字串 ""
"測試中文"
"Hello World"

# 布林值
True
False

# 有順序、可動的列表 List []
[3,4,5]
["Hello","World"] # 可以放字串

# 有順序、不可動的列表 Tuple () 
(3,4,5)
("Hello","World")

# 集合 Set{}
{3,4,5}
"Hello","World"

# 字典 Dictionary {"":""}
{"apple":"蘋果","data":"資料"}

